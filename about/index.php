        <p>Inspired by the <a href="http://loveourcities.org/">Love Our Cities</a> movement started in Modesto, California, Love La Mirada began with the support of a dozen local church leaders who met together to consider how they might challenge their congregations to partner with other organizations and residents of La Mirada for the good of our city. Others joined together to form a team to start up an official organization dedicated to helping people discover more about our city, serve together to improve our neighborhoods, and connect with other residents of La Mirada into greater community.</p>
